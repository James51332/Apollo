#include "Matrix.h"
#include "Vector.h"

namespace Apollo
{

  ////////////////////////////////////////////////////////////
  ///// Vector2 and Scalar ///////////////////////////////////
  ////////////////////////////////////////////////////////////

  Vector2 operator*(Scalar scalar, const Vector2 &vector)
  {
    return vector.Scale(scalar);
  }

  ////////////////////////////////////////////////////////////
  ///// Vector3 And Scalar ///////////////////////////////////
  ////////////////////////////////////////////////////////////

  Vector3 operator*(Scalar scalar, const Vector3 &vector)
  {
    return vector.Scale(scalar);
  }

  ////////////////////////////////////////////////////////////
  ///// Vector4 And Scalar ///////////////////////////////////
  ////////////////////////////////////////////////////////////

  Vector4 operator*(Scalar scalar, const Vector4 &vector)
  {
    return vector.Scale(scalar);
  }

  ////////////////////////////////////////////////////////////
  ///// Matrix3 And Scalar ///////////////////////////////////
  ////////////////////////////////////////////////////////////

  Matrix3 operator*(const Scalar scalar, const Matrix3 &matrix)
  {
    return matrix * scalar;
  }

  ////////////////////////////////////////////////////////////
  ///// Matrix4 And Scalar ///////////////////////////////////
  ////////////////////////////////////////////////////////////

  Matrix4 operator*(const Scalar scalar, const Matrix4 &matrix)
  {
    return matrix * scalar;
  }

} // namespace Apollo